const createBidirectional = (edges: Edge[]): Edge[] => {
  const result: Edge[] = [];
  for (const edge of edges) {
    result.push(edge);
    result.push({
      ...edge,
      source: edge.target,
      target: edge.source,
      waypoints: edge.waypoints ? [...edge.waypoints].reverse() : undefined,
    });
  }
  return result;
};

